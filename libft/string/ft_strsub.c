#include "libft.h"

char	*ft_strsub(char const *s, unsigned int start, size_t len)
{
	char	*str;

	str = ft_strnew(len);
	if (!s || !str)
		return (NULL);
	str = ft_strncpy(str, (char *)s + start, len);
	return (str);
}
